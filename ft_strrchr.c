#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*s1;

	i = ft_strlen(s);
	s1 = (char*)s;
	while (i >= 0)
	{
		if (*(s1 + i) == (char)c)
			return ((s1 + i));
		i--;
	}
	return (NULL);
}
