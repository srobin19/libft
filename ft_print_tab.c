#include "libft.h"

void	ft_print_tab(char **tab)
{
	size_t	i;

	if (!tab)
		return ;

	i = 0;
	while (tab[i])
	{
		ft_putstr(tab[i]);
		ft_putchar('\n');
		i++;
	}
}
