#include "libft.h"

char		*ft_strdup(const char *s1)
{
	char	*copy;
	int		i;
	int		size;

	i = 0;
	size = ft_strlen(s1);
	if (!(copy = (char*)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	while (i < size)
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
